public class MyClass {
  public static void main(String[] args)
  {
    int a = 0;
    while(a <= 10)
    {
      System.out.println(a);
      a++;
    }
    System.out.print("-----------------------------------------------------\n");
    int b = 0;
    do {
      System.out.println(b);
      b++;
    } while(b <= 10);
  }
}