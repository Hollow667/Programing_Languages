public class MyClass {
  public static void main(String[] args)
  {
    int x, y, answer;
    x = 20; y = 30;

    answer = x + y;
    System.out.println("Answer = " + answer);

    answer = x - y;
    System.out.println("Answer = " + answer);

    answer = x * y;
    System.out.println("Answer = " + answer);

    answer = x / y;
    System.out.println("Answer = " + answer);

    answer = x % y;
    System.out.println("Answer = " + answer);

  }
}